// array
val arr = arrayOf(2,4,6,8,22,32)

// list
val list = listOf("a", "b", "d", "g", "k")

fun main() {
 //loop to print array
    println("Array:")
    for (element in arr) {
        println(element)
    }
//loop to print list
    println("list:")
    for (element in list) {
        println(element)
    }
}
